package pe.com.ladc.enums;

public enum GameCategory {

    /**
     * Juegos que se centran en desafíos físicos, como combates o reflejos.
     */
    ACTION,

    /**
     * Juegos de rol con desarrollo de personajes y narrativas profundas.
     */
    RPG,

    /**
     * Juegos que requieren planificación y pensamiento táctico para superar desafíos.
     */
    STRATEGY,

    /**
     * Juegos basados en deportes reales o ficticios.
     */
    SPORTS,

    /**
     * Juegos centrados en la exploración, la resolución de acertijos y la historia.
     */
    ADVENTURE,

    /**
     * Juegos que ponen a prueba la agudeza mental, como puzles y acertijos.
     */
    PUZZLE,

    /**
     * Juegos que simulan actividades de la vida real o profesiones.
     */
    SIMULATION,

    /**
     * Juegos diseñados para un juego rápido y con objetivos simples.
     */
    CASUAL,

    /**
     * Juegos en los que los jugadores compiten entre sí en tiempo real en una arena.
     */
    MOBA,

    /**
     * Juegos que se centran en la construcción y gestión de ciudades, imperios o parques.
     */
    BUILDING,

    /**
     * Juegos que se juegan y se mantienen a través de una conexión a internet.
     */
    MMO,

    /**
     * Videojuegos que se centran en la experiencia de la historia y el diálogo.
     */
    VISUAL_NOVEL
}

